# Crie uma lista vazia chamada de frutas.
# Peça para o usuário digitar o nome de uma fruta e use .append() para adicioná-las à lista.
# Mostre a lista no final.

# RESOLVIDO:

frutas = [] # criamos a lista vazia
fruta = input("Digite uma fruta: ") # solocitamos ao usuário a informação
frutas.append(fruta) # adicionamos a fruta infromada na lista

print(frutas) # apresentamos a lista